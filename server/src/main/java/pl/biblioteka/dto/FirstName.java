package pl.biblioteka.dto;

public class FirstName {
	private String ona;
	private String on;
	public FirstName() {}
	public FirstName(String ona, String on) {
		this.ona=ona;
		this.on=on;
	}
	public String getOna() {
		return ona;
	}
	public void setOna(String ona) {
		this.ona = ona;
	}
	public String getOn() {
		return on;
	}
	public void setOn(String on) {
		this.on = on;
	}
 
 
}
